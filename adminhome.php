                        <h4>Facts about the blood donation process</h4>
                        <div class="alert alert-success">         
                            Donating blood is a safe process. A sterile needle is used only once for each donor and then discarded.<br />
                            Blood donation is a simple four-step process: registration, medical history and mini-physical, donation and refreshments.<br />
                            Every blood donor is given a mini-physical, checking the donor's temperature, blood pressure, pulse and hemoglobin to ensure it is safe for the donor to give blood.<br />
                            The actual blood donation typically takes less than 10-12 minutes. The entire process, from the time you arrive to the time you leave, takes about an hour and 15 min.<br />
                            The average adult has about 10 units of blood in his body. Roughly 1 unit is given during a donation.<br />
                            A healthy donor may donate red blood cells every 56 days, or double red cells every 112 days.<br />
                            A healthy donor may donate platelets as few as 7 days apart, but a maximum of 24 times a year.<br />
                            All donated blood is tested for HIV, hepatitis B and C, syphilis and other infectious diseases before it can be transfused to patients.
                            
